import SwiftUI

struct NutrientCardView: View {
    let card: NutrientInfo
    
    var body: some View {
        VStack(alignment: .leading, spacing: 10) {
            Text(card.todayConsumption)
                .font(.headline)
                .padding(.bottom, 5)
            Text(card.weeklyConsumption)
            Text(card.monthlyConsumption)
            Text(card.recommendedIntake)
            Text(card.foodsRichInNutrient)
            Text(card.benefits)
        }
        .padding()
        .background(Color.white.opacity(0.1))
        .cornerRadius(10)
    }
}
